from __future__ import annotations

from pangolin_eval.models import Completion, ModelTarget, PromptCase
from pangolin_eval.providers.base import Provider
from pangolin_eval.scoring import estimate_message_tokens, estimate_tokens


class MockProvider(Provider):
    def complete(self, model: ModelTarget, prompt: PromptCase) -> Completion:
        mock_responses = model.extra.get("mock_responses", {})
        response = (
            mock_responses.get(prompt.id)
            or model.mock_response
            or "Mock response generated by pangolin-eval."
        )
        return Completion(
            text=response,
            input_tokens=estimate_message_tokens(prompt.messages),
            output_tokens=estimate_tokens(response),
            latency_ms=model.mock_latency_ms or 0,
            metadata={"provider": "mock"},
        )
